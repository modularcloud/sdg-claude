/** A minimal valid single-section source: one node `<id>` under the root. */
function mdxSection(id: string): string {
  return `<S id="${id}">\nText for ${id}.\n</S>\n`;
}

/** Stage a fresh workspace, run `body`, dispose (H-1). */
async function withWorkspace<T>(
  decl: WorkspaceDecl,
  body: (workspace: TestWorkspace) => Promise<T>,
): Promise<T> {
  const workspace = await TestWorkspace.create(decl);
  try {
    return await body(workspace);
  } finally {
    await workspace.dispose();
  }
}

/**
 * Stage a workspace with the given configuration and source files and assert
 * `build --json` refuses it per 14.14 (module header: the deviation is the
 * only defect, so wrong acceptance surfaces as a failed exit-code assertion).
 */
async function expectConfigRefused(
  product: ProductBinding,
  config: string,
  files: Readonly<Record<string, string>>,
  context: string,
): Promise<void> {
  await withWorkspace(
    { files: { "xspec.config.ts": config, ...files } },
    async (workspace) => {
      await expectConfigurationError(product, workspace, ["build"], context);
    },
  );
}

/**
 * Render one `coverage`/`policy` entry list as configuration text: each entry
 * is a list of raw field lines, so every matrix arm states its full literal
 * and the single deviation is visible in the arm itself.
 */
function entriesBlock(
  key: "coverage" | "policy",
  entries: readonly (readonly string[])[],
): string {
  const rendered = entries
    .map(
      (fields) =>
        `    {\n${fields.map((field) => `      ${field}`).join(",\n")}\n    }`,
    )
    .join(",\n");
  return `  ${key}: [\n${rendered}\n  ]`;
}

