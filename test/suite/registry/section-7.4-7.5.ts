/** Covered rows as sorted {identity, path} pairs (each fixture path is the
 * single possible covering path, so no 12.0 tie-break is in play). */
function coveredWithPaths(
  profile: CoverageProfileReport,
): { identity: string; path: readonly string[] }[] {
  return profile.covered
    .map((node) => ({ identity: node.identity, path: node.path }))
    .sort((a, b) => (a.identity < b.identity ? -1 : 1));
}

