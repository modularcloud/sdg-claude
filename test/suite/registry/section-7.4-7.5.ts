/**
 * Run `check --json` expecting findings (exit 1, one JSON document as the
 * entire stdout, H-5) and return the decoded findings (H-3).
 */
async function checkFindings(
  product: ProductBinding,
  workspace: TestWorkspace,
  context: string,
): Promise<readonly Finding[]> {
  const result = await expectExit(
    product,
    workspace,
    ["check", "--json"],
    1,
    `${context} — policy violations are findings of check and cause exit 1 ` +
      `(SPEC 7.5, 14.12, 12.0)`,
  );
  return decodeFindingsReport(parseJsonStdout(result, context), context)
    .findings;
}

/** One expected policy finding: the rule name plus the offending edge. */
interface PolicyExpectation {
  readonly rule: string;
  readonly edge: GraphEdge;
}

function renderPolicyPair(rule: string, edge: GraphEdge): string {
  return `${rule} :: ${edge.kind}: ${edge.from} -> ${edge.to}`;
}

/**
 * Assert a findings report carries exactly the expected policy findings: the
 * condition multiset is exactly `14.12 × n` (nothing masked, nothing phantom)
 * and the (rule, offending edge) pairs match, order-insensitively (module
 * header: SPEC 7.5 fixes the information, not an order).
 */
function assertPolicyFindings(
  findings: readonly Finding[],
  expected: readonly PolicyExpectation[],
  context: string,
): void {
  assertConditionCounts(
    findings,
    expected.length === 0 ? {} : { "14.12": expected.length },
    context,
  );
  assertSameJson(
    findings
      .map(
        (finding) =>
          `${finding.rule ?? "<no rule>"} :: ` +
          (finding.edge === undefined
            ? "<no edge>"
            : `${finding.edge.kind}: ${finding.edge.from} -> ${finding.edge.to}`),
      )
      .sort(),
    expected.map((entry) => renderPolicyPair(entry.rule, entry.edge)).sort(),
    `${context}: policy findings as (rule name, offending edge) pairs ` +
      `(SPEC 7.5, 14.12)`,
  );
}

/**
 * The staged-policy protocol (module header): `build` must succeed — sources
 * are valid and policy never fails a build (12.1) — so the following `check`
 * runs over fresh generated output and its findings are exactly the staged
 * policy violations.
 */
async function expectPolicyFindings(
  product: ProductBinding,
  files: Readonly<Record<string, string>>,
  expected: readonly PolicyExpectation[],
  contextBase: string,
): Promise<void> {
  await withWorkspace({ files }, async (workspace) => {
    await buildOk(
      product,
      workspace,
      `${contextBase} \`build\` — sources are valid and build does not ` +
        `evaluate policy (SPEC 12.1, 7.5), so the check below observes ` +
        `fresh output and only policy findings`,
    );
    const label = `${contextBase} \`check --json\``;
    assertPolicyFindings(
      await checkFindings(product, workspace, label),
      expected,
      label,
    );
  });
}

/** Resolve one profile of a coverage report by name, diagnosed (H-8). */
function profileNamed(
  report: CoverageReport,
  name: string,
  context: string,
): CoverageProfileReport {
  const profile = report.profiles.find((candidate) => candidate.name === name);
  if (profile === undefined) {
    fail(
      `${context}: the coverage report must carry profile ${JSON.stringify(name)} — ` +
        `all configured profiles run by default (SPEC 8.2); got profiles ` +
        `${JSON.stringify(report.profiles.map((candidate) => candidate.name))}`,
    );
  }
  return profile;
}

