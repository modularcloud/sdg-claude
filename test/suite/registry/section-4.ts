/**
 * Run one invalid-import arm: `build --json` exits 1 with only 14.15
 * findings — exactly one for a single-statement arm, one or two for a
 * colliding pair — each locating the importing code file and falling within
 * an offending statement's own byte window (SPEC 14: errors identify file
 * and location).
 */
async function runInvalidTsImportArm(
  product: ProductBinding,
  arm: InvalidTsImportArm,
  testId: string,
): Promise<void> {
  const context = `${testId} \`build --json\` over ${arm.name}`;
  const windows: { start: number; end: number }[] = [];
  let prefix = "";
  for (const statement of arm.statements) {
    windows.push(byteWindow(prefix, statement));
    prefix += statement + "\n";
  }
  await withWorkspace(
    arm.config ?? SPEC_AND_CODE_CONFIG,
    {
      ...T4_2_BASE_FILES,
      ...arm.extraFiles,
      "src/app.ts": prefix,
    },
    async (workspace) => {
      const findings = await buildFindings(product, workspace, context);
      const max = arm.maxFindings ?? 1;
      if (max === 1) {
        assertConditionCounts(findings, { "14.15": 1 }, context);
        assertFindingLocated(
          findings[0]!,
          { file: "src/app.ts", window: windows[0]! },
          `${context}: the 14.15 finding`,
        );
        return;
      }
      const conditions = findings.map((finding) => finding.condition);
      if (
        findings.length < 1 ||
        findings.length > max ||
        conditions.some((condition) => condition !== "14.15")
      ) {
        fail(
          `${context}: expected the colliding pair to report condition 14.15 — ` +
            `one finding for the collision, or one per import — got ` +
            JSON.stringify(conditions),
        );
      }
      for (const finding of findings) {
        const findingContext = `${context}: a 14.15 finding`;
        assertFindingLocated(finding, { file: "src/app.ts" }, findingContext);
        const { location } = finding;
        const within = windows.some(
          (window) =>
            location !== undefined &&
            location.start >= window.start &&
            location.end <= window.end,
        );
        if (!within) {
          fail(
            `${findingContext}: its location [${String(location?.start)}, ` +
              `${String(location?.end)}) must point at one of the colliding ` +
              `import statements (byte windows ${JSON.stringify(windows)})`,
          );
        }
      }
    },
  );
}

