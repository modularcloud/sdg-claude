// Two spec sources with distinct names, one in a subdirectory, so each
// generated module's header must identify its *own* source file — a constant
// header copied between modules fails the per-module name assertion.
const T4_1_FILES = {
  "specs/A.mdx": '<S id="alpha">\nAlpha behavior.\n</S>\n',
  "specs/sub/B.mdx": '<S id="beta">\nBeta behavior.\n</S>\n',
} as const;

/**
 * The module's leading comment region: line and block comments, with
 * interleaving whitespace, from the start of the file up to the first
 * non-comment code. An unterminated block comment does not extend the
 * region (it is not a well-formed header).
 */
function leadingCommentRegion(moduleText: string): string {
  let at = 0;
  let end = 0;
  while (at < moduleText.length) {
    if (/\s/.test(moduleText[at]!)) {
      at += 1;
      continue;
    }
    if (moduleText.startsWith("//", at)) {
      const lineEnd = moduleText.indexOf("\n", at);
      at = lineEnd === -1 ? moduleText.length : lineEnd + 1;
      end = at;
      continue;
    }
    if (moduleText.startsWith("/*", at)) {
      const close = moduleText.indexOf("*/", at + 2);
      if (close === -1) break;
      at = close + 2;
      end = at;
      continue;
    }
    break;
  }
  return moduleText.slice(0, end);
}

/** Read a product-generated module as UTF-8 text, failing diagnosed (H-8). */
async function readGeneratedModule(
  workspace: TestWorkspace,
  rel: string,
  context: string,
): Promise<string> {
  const kind = await workspace.kind(rel);
  if (kind !== "file") {
    fail(
      `${context}: expected the generated module as a plain file at ${rel} ` +
        `(SPEC 13.1: NAME.mdx generates NAME.xspec.ts in the source file's ` +
        `directory); found ${kind}`,
    );
  }
  const bytes = await workspace.readBytes(rel);
  try {
    return new TextDecoder("utf-8", { fatal: true }).decode(bytes);
  } catch {
    fail(`${context}: the generated module at ${rel} is not valid UTF-8`);
  }
}

